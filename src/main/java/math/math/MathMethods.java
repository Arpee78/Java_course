package math.math;

public class MathMethods {

    public static void main(String[] args) {

        System.out.println(Math.max(1,2));
        System.out.println(Math.min(1,2));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(7.8));
        System.out.println(Math.abs(-10));
        System.out.println(Math.negateExact(-10));
        System.out.println(Math.addExact(10,7));
        System.out.println(Math.subtractExact(10,7));
        System.out.println(Math.multiplyExact(10,7));
        System.out.println(Math.pow(2.2,3));
        System.out.println(Math.pow(3.1,3));
        System.out.println(Math.incrementExact(3));
        System.out.println(Math.decrementExact(3));
        System.out.println(Math.random());

    }
}
